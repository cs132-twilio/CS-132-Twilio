      <div id="content">
	<p>The <a href="dashboard">dashboard</a> is your gateway to managing classes, interacting 
	  with students, and creating educational content to share with them.</p>
	<h2>Managing Classes</h2>
	<p>Use the "Manage Classes" button on the left side to access class management. Here you can add new classes and remove existing ones.</p>
	<p>When you make a class, you can see its members on the "List" tab. On this page,
	a class id number will be displayed at the bottom. Students can join classes once they
	are registered on twexter. To register, have them text your number (which is displayed
	in the top navigation bar) with "join [their name]". Once they are registered,
	they can text "join [class id number]" to your number to join that class. 
	You can remove students from a class by hitting the "x" to the right of their name
	on the List page.</p>
	<h2>Messaging Students</h2>
	<p>On the "Message" tab, you can send messages to individual students, or multiple students,
	or whole classes of students. Type the name of a student or class and while you type
	the name will autocomplete. When you have selected all recipients, enter your 
	message text and click send.</p>
	</p>Students can message you if they are registered by texting your number with 
	"m [message content]" or "message [message content]". You can read these messages by
	clicking "Inbox" in the top navigation bar.</p>
	<h2>Streams</h2>
	<p>To set up a live stream displaying incoming texts from students in a class,
	go to the Stream tab, and click "add new" to create a new stream. When this
	stream is selected it will display its id number. Have students text
	"s [stream id] [content]" or "str [stream id] [content]" or "stream [stream id] [content]".</p>
	<h2>Polls</h2>
	<p>To create a new poll for a class, give it a name, a poll type (multiple choice or free response) and a duration. Students can participate in the poll by texting "['p' or 'poll'] [poll name] [a/b/c/d if multiple choice (case insensitive) or any content if free response]"</p>
	<p>To view responses, select a poll and click "view". Multiple choice polls are displayed 
	as pie chart graphs that live update.</p>
	<h2>Flashcards</h2>
	<p>To create a new "deck" of cards, click "add new deck" then give it a name and click "add". To add a new card, click "add new card" and select a deck to which it should be added, and
	type the question and answer (each limited to 145 characters). Cards can be deleted by selecting a deck at the top to view its cards and then selecting those to be deleted and clicking 
	"delete selected cards."</p>
	<p>Texting "[f/fl/flash]" without anything else will list the available decks. Texting "[f/fl/flash] [deck name]" will show the current card. Twexter keeps track of which card
	in a deck a student is viewing. To navigate the deck the student can type "[f/fl/flash] [deck name] [command]" where "command" is either "next" which goes to the next card in the deck,
	"flip" which shows the answer to the current question or the question to the current answer,
	"reset" which goes to the first question, or a number, which goes to that number question in the deck.</p>

	  
